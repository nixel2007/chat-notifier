#!/bin/sh
set -e

# Railway (и любой PaaS) передаёт порт через переменную окружения PORT.
# winow читает порт как детальку "winow.Порт" (autumn, ENV-провайдер имеет
# наивысший приоритет). Провайдер преобразует имя переменной среды по правилу
# "_" -> ".", поэтому ключу winow.Порт соответствует переменная winow_Порт.
# Имя переменной кириллическое, задаём его через env(1) (export его не примет).
: "${PORT:=3333}"

cd /opt/chat-notifier
echo "chat-notifier: слушаю порт ${PORT}"
exec env "winow_Порт=${PORT}" oscript src/main.os
